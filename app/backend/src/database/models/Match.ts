import { Model, INTEGER } from 'sequelize';
import db from '.';
import Teams from './Team';

class Matches extends Model {
  id!: number;
  homeTeam!: number;
  homeTeamGoals!: number;
  awayTeam!: number;
  awayTeamGoals: number;
  inProgress!: number;
}

Matches.init({
  id: {
    allowNull: false,
    autoIncrement: true,
    primaryKey: true,
    type: INTEGER,
  },
  homeTeam: {
    allowNull: false,
    type: INTEGER,
  },
  homeTeamGoals: {
    allowNull: false,
    type: INTEGER,
  },
  awayTeam: {
    allowNull: false,
    type: INTEGER,
  },
  awayTeamGoals: {
    allowNull: false,
    type: INTEGER,
  },
  inProgress: {
    allowNull: false,
    type: INTEGER,
  },
}, {
  // ... Outras configs
  underscored: true,
  sequelize: db,
  modelName: 'Matches',
  timestamps: false,
});

Matches.hasMany(Teams, { foreignKey: 'id', as: 'home_team' });
Matches.hasMany(Teams, { foreignKey: 'id', as: 'away_team' });

Teams.belongsTo(Matches, { foreignKey: 'home_team', as: 'home_team' });
Teams.belongsTo(Matches, { foreignKey: 'away_team', as: 'away_team' });

